// ─── Type resolution per SObject ──────────────────────────────────────────────

/**
 * Decide how a generated route should refer to the record type for an SObject.
 * Standard SObjects use the pre-built type from `ballerinax/salesforce.types`,
 * which eliminates 95%+ of generated record boilerplate. Custom objects fall
 * back to a record we generate from the describe payload.
 */
export function recordTypeFor(objectName: string): {
  prebuilt: boolean;
  typeRef: string;
} {
  if (isCustomObject(objectName)) {
    return { prebuilt: false, typeRef: `${customRecordName(objectName)}` };
  }
  return { prebuilt: true, typeRef: `sftypes:${objectName}SObject` };
}

function customRecordName(objectName: string): string {
  // MyPkg__Foo__c → MyPkg_Foo_Record (Ballerina identifier-safe)
  return (
    objectName
      .replace(/__c$/i, "")
      .replace(/[^A-Za-z0-9]/g, "_")
      .replace(/^_+|_+$/g, "") + "Record"
  );
}

// ─── types.bal — custom record types only ────────────────────────────────────

export interface TypesFileResult {
  content: string;
  truncated: Array<{ object: string; total: number; emitted: number }>;
}

export function generateTypesFile(
  customDescribes: SObjectDescribeResult[]
): TypesFileResult {
  const truncated: TypesFileResult["truncated"] = [];

  if (customDescribes.length === 0) {
    return {
      content: `// Auto-generated by ballerina-salesforce-mcp-server.
// Standard SObjects use pre-built types from ballerinax/salesforce.types.
// Custom record types (if any) are declared below.
`,
      truncated,
    };
  }

  const lines: string[] = [
    "// Auto-generated by ballerina-salesforce-mcp-server",
    "// Custom SObject record type definitions",
    "",
  ];

  for (const obj of customDescribes) {
    const totalFields = obj.fields.length;
    const fields = obj.fields.slice(0, MAX_FIELDS_PER_TYPE);
    if (totalFields > MAX_FIELDS_PER_TYPE) {
      truncated.push({
        object: obj.name,
        total: totalFields,
        emitted: MAX_FIELDS_PER_TYPE,
      });
    }

    lines.push(`// ${obj.label} (${obj.name})`);
    if (totalFields > MAX_FIELDS_PER_TYPE) {
      lines.push(
        `// NOTE: truncated to first ${MAX_FIELDS_PER_TYPE} of ${totalFields} fields.`
      );
    }
    lines.push(`public type ${customRecordName(obj.name)} record {`);
    lines.push(`    string? Id = ();`);

    for (const f of fields) {
      if (f.name === "Id") continue;
      const balType = sfTypeToBal(f.type, f.nillable);
      lines.push(
        `    ${balType} ${sanitizeFieldName(f.name)} = ();   // ${f.label}`
      );
    }

    lines.push(`};`);
    lines.push("");
  }

  return { content: lines.join("\n"), truncated };
}

// ─── main.bal — service entry-point and shared SF client ─────────────────────

export function generateMainBal(
  orgName: string,
  packageName: string,
  objectNames: string[],
  servicePort: number = DEFAULT_SERVICE_PORT
): string {
  // We always import the types submodule — it's harmless if unused, and
  // keeps the file consistent across standard/custom mixes.
  const hasPrebuilt = objectNames.some((o) => !isCustomObject(o));

  const typesImport = hasPrebuilt
    ? `import ballerinax/salesforce.types as sftypes;`
    : "";

  const routes = objectNames
    .map((o) => {
      const snake = snakeCase(o);
      const { typeRef } = recordTypeFor(o);
      return `    // ${o} routes
    resource function get ${snake}s() returns json|error {
        return check query${safeId(o)}Records();
    }
    resource function get ${snake}/[string id]() returns json|error {
        return check get${safeId(o)}RecordById(id);
    }
    resource function post ${snake}(${typeRef} body) returns json|error {
        return check create${safeId(o)}Record(body);
    }
    resource function put ${snake}/[string id](${typeRef} body) returns json|error {
        return check update${safeId(o)}Record(id, body);
    }
    resource function delete ${snake}/[string id]() returns json|error {
        return check delete${safeId(o)}Record(id);
    }`;
    })
    .join("\n\n");

  return `import ballerina/http;
import ballerina/log;
import ballerina/os;
import ballerinax/salesforce;
${typesImport}

// ── Configurable variables (Config.toml or env vars) ────────────────────────
// Falls back to environment variables so the same project runs locally,
// in containers, or in WSO2 BI runtime without editing Config.toml.
configurable string clientId     = os:getEnv("SF_CLIENT_ID");
configurable string clientSecret = os:getEnv("SF_CLIENT_SECRET");
configurable string refreshToken = os:getEnv("SF_REFRESH_TOKEN");
configurable string refreshUrl   = os:getEnv("SF_REFRESH_URL");
configurable string baseUrl      = os:getEnv("SF_BASE_URL");
configurable string apiVersion   = "${SF_API_VERSION}";
configurable int    servicePort  = ${servicePort};

// ── Startup validation ───────────────────────────────────────────────────────
// Fail fast with a clear message if credentials are missing, instead of
// surfacing an opaque connector error on the first request.
function init() returns error? {
    if clientId == "" || clientSecret == "" || refreshToken == "" || baseUrl == "" {
        return error("Missing Salesforce credentials. Set them in Config.toml or via " +
            "env vars: SF_CLIENT_ID, SF_CLIENT_SECRET, SF_REFRESH_TOKEN, SF_BASE_URL " +
            "(refreshUrl optional — defaults to login.salesforce.com).");
    }
    log:printInfo("Starting Salesforce integration", servicePort = servicePort, baseUrl = baseUrl);
}

// ── Salesforce client ────────────────────────────────────────────────────────

final salesforce:ConnectionConfig sfConfig = {
    baseUrl: baseUrl,
    auth: {
        clientId:     clientId,
        clientSecret: clientSecret,
        refreshToken: refreshToken,
        refreshUrl:   refreshUrl
    },
    apiVersion: apiVersion
};

final salesforce:Client sfClient = check new (sfConfig);

// ── HTTP listener ────────────────────────────────────────────────────────────
listener http:Listener httpListener = new (servicePort);

// ── Service router ───────────────────────────────────────────────────────────
service / on httpListener {

    resource function get health() returns json {
        log:printInfo("Health check OK");
        return { status: "UP", package: "${orgName}/${packageName}" };
    }

${routes}
}
`;
}

/**
 * Per-object CRUD file. Lives in the same default module as main.bal,
 * so the shared `sfClient` and `sftypes:` import are visible.
 */
export function generateObjectModule(objectName: string): string {
  const { prebuilt, typeRef } = recordTypeFor(objectName);
  const fnSuffix = safeId(objectName);
  const typesImport = prebuilt
    ? "// Uses pre-built record type from ballerinax/salesforce.types (imported in main.bal)."
    : "// Uses generated custom record type defined in types.bal.";

  return `// ── ${objectName} CRUD operations ───────────────────────────────────────────
${typesImport}

public function query${fnSuffix}Records() returns json|error {
    string soql = "SELECT FIELDS(STANDARD) FROM ${objectName} LIMIT 200";
    stream<record {}, error?> resultStream = check sfClient->query(soql);
    record {}[] records = [];
    check from record {} rec in resultStream
        do {
            records.push(rec);
        };
    return records.toJson();
}

public function get${fnSuffix}RecordById(string id) returns json|error {
    ${typeRef} rec = check sfClient->getById("${objectName}", id);
    return rec.toJson();
}

public function create${fnSuffix}Record(${typeRef} payload) returns json|error {
    var res = check sfClient->create("${objectName}", payload);
    return { id: res.id, success: true };
}

public function update${fnSuffix}Record(string id, ${typeRef} payload) returns json|error {
    check sfClient->update("${objectName}", id, payload);
    return { id: id, success: true };
}

public function delete${fnSuffix}Record(string id) returns json|error {
    check sfClient->delete("${objectName}", id);
    return { id: id, success: true };
}
`;
}

// ─── CDC / Platform Events listener ──────────────────────────────────────────

export type CdcEventType = "onCreate" | "onUpdate" | "onDelete" | "onRestore";

export interface CdcListenerOptions {
  /** SObject API name for CDC, e.g. "Account". Mutually exclusive with `allChanges` and `platformEvent`. */
  sobject?: string;
  /** Listen on /data/ChangeEvents (all CDC-enabled objects). */
  allChanges?: boolean;
  /** Platform event API name (ends with __e), e.g. "MyEvent__e". */
  platformEvent?: string;
  /** Which CDC callbacks to scaffold. Defaults to all four. Ignored for platform events. */
  events?: CdcEventType[];
}

/**
 * Generates a Ballerina file declaring a `salesforce:Listener` and either a
 * `CdcService` or `PlatformEventsService` for the chosen channel. The shared
 * SF credentials configured in main.bal are reused.
 *
 * Channel name follows the WSO2 BI docs:
 *   - Object CDC:       /data/<SObjectName>ChangeEvent
 *   - All CDC events:   /data/ChangeEvents
 *   - Platform event:   /event/<Name>__e
 */
export function generateCdcListener(opts: CdcListenerOptions): {
  filename: string;
  content: string;
} {
  if (opts.platformEvent) {
    return generatePlatformEventListener(opts.platformEvent);
  }

  const channel = opts.allChanges
    ? "/data/ChangeEvents"
    : `/data/${opts.sobject}ChangeEvent`;

  const events: CdcEventType[] =
    opts.events && opts.events.length > 0
      ? opts.events
      : ["onCreate", "onUpdate", "onDelete", "onRestore"];

  const label = opts.allChanges ? "AllChangeEvents" : opts.sobject!;
  const filename = `cdc_${snakeCase(label)}.bal`;

  const callbacks = events
    .map(
      (e) => `    remote function ${e}(salesforce:EventData event) returns error? {
        log:printInfo("${label} ${e}", event = event);
    }`
    )
    .join("\n\n");

  const content = `// ── CDC listener for ${channel} ─────────────────────────
import ballerina/log;
import ballerinax/salesforce;

// Reuses the OAuth2 credentials configured in main.bal (clientId/secret/refreshToken/refreshUrl/baseUrl).
listener salesforce:Listener ${cdcListenerVar(label)} = new ({
    auth: {
        clientId:     clientId,
        clientSecret: clientSecret,
        refreshToken: refreshToken,
        refreshUrl:   refreshUrl
    },
    baseUrl: baseUrl,
    channelName: "${channel}"
});

service salesforce:CdcService on ${cdcListenerVar(label)} {
${callbacks}
}
`;

  return { filename, content };
}

function generatePlatformEventListener(eventName: string): {
  filename: string;
  content: string;
} {
  const channel = `/event/${eventName}`;
  const safeName = safeId(eventName);
  const filename = `event_${snakeCase(eventName)}.bal`;

  const content = `// ── Platform Event listener for ${channel} ─────────────
import ballerina/log;
import ballerinax/salesforce;

listener salesforce:Listener platformEventListener_${safeName} = new ({
    auth: {
        clientId:     clientId,
        clientSecret: clientSecret,
        refreshToken: refreshToken,
        refreshUrl:   refreshUrl
    },
    baseUrl: baseUrl,
    channelName: "${channel}"
});

service salesforce:PlatformEventsService on platformEventListener_${safeName} {
    remote function onMessage(salesforce:PlatformEventsMessage event) returns error? {
        log:printInfo("Platform event ${eventName}", event = event);
    }
}
`;

  return { filename, content };
}

function cdcListenerVar(label: string): string {
  return `cdcListener_${safeId(label)}`;
}

