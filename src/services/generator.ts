// ─── Utility helpers ──────────────────────────────────────────────────────────

export function snakeCase(name: string): string {
  return name
    .replace(/__c$/i, "")
    .replace(/([A-Z])/g, "_$1")
    .toLowerCase()
    .replace(/^_/, "")
    .replace(/__+/g, "_");
}

/**
 * Identifier-safe transform for function names (e.g. MyPkg__Foo__c → MyPkg_Foo).
 * Exported so the tools layer can mirror what the generator emits without
 * duplicating the regex (single source of truth).
 */
export function safeId(objectName: string): string {
  return objectName
    .replace(/__c$/i, "")
    .replace(/[^A-Za-z0-9]/g, "_")
    .replace(/_+/g, "_")
    .replace(/^_+|_+$/g, "");
}

/**
 * Ballerina reserved words that cannot be used as bare identifiers.
 * When a Salesforce field name collides with one of these, we prefix it
 * with a single quote so Ballerina treats it as a quoted identifier.
 * Reference: https://ballerina.io/spec/lang/master/#section_3.1
 */
const BAL_KEYWORDS = new Set([
  "abstract", "anydata", "boolean", "break", "byte", "check", "checkpanic",
  "class", "client", "commit", "configurable", "conflict", "const",
  "decimal", "distinct", "do", "else", "enum", "error", "fail", "false",
  "final", "float", "flush", "fork", "from", "function", "future",
  "handle", "if", "import", "in", "int", "intersect", "is", "isolated",
  "join", "json", "key", "let", "limit", "listener", "lock", "map",
  "match", "module", "never", "new", "null", "object", "on", "order",
  "outer", "panic", "readonly", "record", "remote", "resource", "retry",
  "return", "returns", "rollback", "select", "service", "start", "stream",
  "string", "table", "transaction", "trap", "true", "type", "typedesc",
  "union", "var", "wait", "where", "while", "worker", "xml",
]);

function sanitizeFieldName(name: string): string {
  const sanitized = name.replace(/__c$/i, "").replace(/[^a-zA-Z0-9_]/g, "_");
  // Escape reserved words so generated Ballerina code compiles cleanly.
  return BAL_KEYWORDS.has(sanitized) ? `'${sanitized}` : sanitized;
}

function sfTypeToBal(sfType: string, nillable: boolean): string {
  const optional = nillable ? "?" : "";
  const mapping: Record<string, string> = {
    string: `string${optional}`,
    textarea: `string${optional}`,
    id: `string${optional}`,
    reference: `string${optional}`,
    picklist: `string${optional}`,
    multipicklist: `string${optional}`,
    phone: `string${optional}`,
    url: `string${optional}`,
    email: `string${optional}`,
    boolean: `boolean${optional}`,
    int: `int${optional}`,
    integer: `int${optional}`,
    double: `decimal${optional}`,
    currency: `decimal${optional}`,
    percent: `decimal${optional}`,
    date: `string${optional}`,
    datetime: `string${optional}`,
    time: `string${optional}`,
    base64: `string${optional}`,
  };
  return mapping[sfType.toLowerCase()] ?? `string${optional}`;
}

function escapeToml(value: string): string {
  return value.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function stripNewlines(value: string): string {
  return value.replace(/[\r\n]+/g, "");
}
